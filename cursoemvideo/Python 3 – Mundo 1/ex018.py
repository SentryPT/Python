#Faça um programa que leia um ângulo e mostre o valor de seno, cosseno e tangente...
import math
ang = float(input('Indique um ângulo à sua escolha: '))
print (f'O valor do ângulo é {ang} e tem como valor de seno {math.sin(ang):.3f}, de cosseno {math.cos(ang):.3f} e de tangente {math.tan(ang):.3f}')

